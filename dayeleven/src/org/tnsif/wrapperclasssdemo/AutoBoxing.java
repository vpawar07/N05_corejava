package org.tnsif.wrapperclasssdemo;
//Program to demonstrate on AutoBoxing
//AutoBoxing: Conversion of Primitive type to Object Type
public class AutoBoxing {

	public static void main(String[] args) {
		char c = 'A';
		//converting primitive data type to object type
		Character ch =c;
		System.out.println(ch);
	}

}
