//Program to demonstrate on UnBoxing
//UnBoxing: Conversion of Object Type type to Primitive

public class UnBoxing {

	public static void main(String[] args) {
		Integer num =34;
		//Conversion Object Type type to Primitive
		int x=num;
		System.out.println(x);
	}

}
